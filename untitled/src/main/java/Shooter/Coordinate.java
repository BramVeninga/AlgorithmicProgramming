package Shooter;

public class Coordinate
{
    private double X;
    private double Y;

    public Coordinate(double x, double y)
    {
        X = x;
        Y = y;
    }

    public double getX()
    {
        return X;
    }

    public void setX(double x)
    {
        X = x;
    }

    public double getY()
    {
        return Y;
    }

    public void setY(double y)
    {
        Y = y;
    }
}
